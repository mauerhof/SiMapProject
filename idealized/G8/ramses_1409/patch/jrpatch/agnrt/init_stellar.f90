subroutine init_stellar_library
   use amr_commons
   use stellar_commons
   implicit none
   real(KIND=8),dimension(1:nbint,1:nbinz)::cR_Ia,cR_HN
   real(KIND=8),dimension(1:nbint)::cR_Ia_1d
   real(KIND=8),dimension(1:nbinz)::eps_HN
   real(KIND=8)::fz_Ia,fOx_Ia,fFe_Ia
   integer::i

   ! Read tables for stellar wind
   call read_stellar_wind_table

   ! Read tables for SNIa and combine it with stellar winds
   if (A_snIa.gt.0D0) then
       call snIa_yield(fz_Ia,fOx_Ia,fFe_Ia)
       if (myid.eq.1)then
          write(*,*)' >>> fz,fOx,fFe [snIa] =', sngl(fz_Ia),sngl(fOx_Ia),sngl(fFe_Ia)
       endif
       call read_snIa_table(cR_Ia_1d)
       do i=1,nbinz
          cR_Ia(1:nbint,i) = cR_Ia_1d(1:nbint)
       end do
       cMZwind  = dlog10(10d0**cMZwind  + A_snIa*cR_Ia*1.44d0*fz_Ia ) ! per Msun
       cMOxwind = dlog10(10d0**cMOxwind + A_snIa*cR_Ia*1.44d0*fOx_Ia) ! per Msun
       cMFewind = dlog10(10d0**cMFewind + A_snIa*cR_Ia*1.44d0*fFe_Ia) ! per Msun
       cMwind   = dlog10(10d0**cMwind   + A_snIa*cR_Ia*1.44d0)        ! per Msun
       cEwind   = dlog10(10d0**cEwind   + A_snIa*cR_Ia*1d51)
   end if

   if (e_HN.ge.1) then
!      eps_HN=(/1.00,0.82,0.62,0.01,0.01/) ! fraction of HNe between 20 < M < 50 Msun
      eps_HN=(/0.50,0.41,0.31,0.01,0.01/)
     call read_HN_table(cR_HN)
      cEwind(:,1) = dlog10( 10d0**cEwind(:,1) + e_HN*1d51*cR_HN(:,1)*eps_HN(1))
      cEwind(:,2) = dlog10( 10d0**cEwind(:,2) + e_HN*1d51*cR_HN(:,2)*eps_HN(2))
      cEwind(:,3) = dlog10( 10d0**cEwind(:,3) + e_HN*1d51*cR_HN(:,3)*eps_HN(3))
      cEwind(:,4) = dlog10( 10d0**cEwind(:,4) + e_HN*1d51*cR_HN(:,4)*eps_HN(4))
      cEwind(:,5) = dlog10( 10d0**cEwind(:,5) + e_HN*1d51*cR_HN(:,5)*eps_HN(5))
   endif

end subroutine init_stellar_library
